/*
*
*Decorator pattern allows a user to add new 
*functionality to an existing object without altering its structure.
*
*In object-oriented programming, the decorator pattern is a design pattern 
*that allows behavior to be added to an individual object, either statically or dynamically, 
*without affecting the behavior of other objects from the same class.[1] The decorator 
*pattern is often useful for adhering to the Single Responsibility Principle, as it allows 
*functionality to be divided between classes with unique areas of concern.[2] The decorator 
*pattern is structurally nearly identical to the chain of responsibility pattern, the 
*difference being that in a chain of responsibility, exactly one of the classes handles the 
*request, while for the decorator, all classes handle the request.
*/